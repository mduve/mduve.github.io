			<h5 class="section-header"><span>The Challenge</span></h5>
			<p>In 2016, the client reached out to me to request a rebuild of its website. After performing an audit of the previous site, it 
				became clear that his "Products" and "Services" lacked segmentation. "Projects" contained little or no content for users to consume. 
				It was imperative the rebrand connected with his base, but also to new users. In addition, 
				the client wanted to make sure that the site was scalable and robust enough to grow with each new project
				but also pivot alongside his marketing team, publishing content through his blog and case studies.</p>
			<h6 class="header">High level goals and Objectives:</h6>
			<ol>
				<li>Satisfy brand requirements as outlined by the client.</li>
				<li>Build a scalable CMS website that could exist as a standalone showcase for "Projects" but 
					support an ever changing list of "Products" and "Services".</li>
			</ol>
			<h5 class="section-header">Design Process</h5>

			<h5 class="section-header-sub"><i class="far fa-check-square"></i> Brand</h5>
			<p>An emphasis was placed on creating a mark that was modern and architectural in style but contained past elements such as color and the brick reference. 
				I offered three different variations of the mark which the client chose from.</p>

