				<h5 class="section-header"><span>Users</span></h5>	
				<h6 class="header">Support Operators</h6>
				<p id="survey">I originally performed <a href="projects/ux-support-dashboard/process-research-1.jpg" target="_blank">interviews</a>, questionnaires, and <a href="https://6pb14e.axshare.com/home.html" target="_blank">user testing</a> with our support team in March of 2016.</p>

				<h5 class="section-header"><span>The Challenge</span></h5>
				<p>Ultimately, I wanted to provide a UI that was scalable while still promoting visibility of open items/print issues. Support operators fully expected an application not too different than what they were already familiar with. The ability to manage printers from the application would only empower them more.</p>

				<h6 class="header">High level goals and Objectives:</h6>
				<ol>
					<li>Provide operators the ability to easily monitor/scan printer support queues.</li>
					<li>Provide operators the ability to search and manage printers remotely.</li>
				</ol>

